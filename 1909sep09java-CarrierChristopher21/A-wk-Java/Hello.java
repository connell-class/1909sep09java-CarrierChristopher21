class Hello {
	//The main method is the entry point into the program
	//when we run the class file, java looks for the main
	//method to see where to start
	
	//the String array is the paramters for the method
	//when we call the method, we give it values,
	//these are known as the arguments
	public static void main(String[] args){
		//we can print args[0] to get the first argument
		System.out.println(99);
		System.out.print(100);
		System.out.println(1);
		/*this is a multi line comment*/
		System.out.println(args[0]);
		
	}
}

